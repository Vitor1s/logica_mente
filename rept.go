package main

import "fmt"

func main() {
    // Começamos com o número 1
    for i := 1; i <= 20; i++ {
        fmt.Println(i) // imprime o número atual
    }
}

// i := 1 → Cria a variável i começando com 1.

// i <= 5 → Enquanto i for menor ou igual a 5, o for continua.

// i++ → Depois de cada repetição, o i aumenta 1.

// { fmt.Println(i) } → Esse é o código que será executado a cada volta.

